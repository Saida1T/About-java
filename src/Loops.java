public class Loops {
    public static void main(String[] args) {
        // Цикл while
        int i = 0;
        while (i < 5) {
            System.out.println("Цикл while: " + i);
            i++;
    }
        //Цикл for
        for (int j = 0; j < 5; j++) {
            System.out.println("Цикл for: " + j);
        }
        }
}
